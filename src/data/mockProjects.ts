// Generate default readiness tasks for a project
const getDefaultReadinessTasks = (projectId: string, options?: { hasPrimaryContact?: boolean; hasSchedule?: boolean; isConfirmed?: boolean; hasDevice?: boolean; hasCrew?: boolean; hasBlockers?: boolean }): ReadinessTask[] => {
  const opts = {
    hasPrimaryContact: true,
    hasSchedule: false,
    isConfirmed: false,
    hasDevice: false,
    hasCrew: false,
    hasBlockers: false,
    ...options
  }

  return [
    { id: `ready-${projectId}-1`, label: 'Customer contact confirmed', completed: opts.hasPrimaryContact, critical: true },
    { id: `ready-${projectId}-2`, label: 'Install date scheduled', completed: opts.hasSchedule, critical: true },
    { id: `ready-${projectId}-3`, label: 'Customer confirmed install date', completed: opts.isConfirmed, critical: true },
    { id: `ready-${projectId}-4`, label: 'Building access instructions available', completed: false },
    { id: `ready-${projectId}-5`, label: 'Device assigned', completed: opts.hasDevice },
    { id: `ready-${projectId}-6`, label: 'Install crew assigned', completed: opts.hasCrew },
    { id: `ready-${projectId}-7`, label: 'No active blockers', completed: !opts.hasBlockers, critical: true },
    { id: `ready-${projectId}-8`, label: 'FOC date confirmed', completed: true },
  ]
}
const formatDate = (daysOffset: number) => {
  const d = new Date(today)
  d.setDate(d.getDate() + daysOffset)
  return d.toISOString()
}

